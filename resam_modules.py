import torch
import torch.nn as nn
import torch.nn.functional as F
import torchvision.ops as ops

# ==============================================================================
# MODULE 1: Entropy-based Mask Refinement and Box Re-querying (Refine & Requery)
# ==============================================================================

def refine_and_requery_keyframe(initial_prob_mask, image, sam_model, epsilon=0.2):
    """
    Refines an initial probability mask using entropy-based filtering and 
    requeries the SAM model with a generated bounding box to obtain a higher-quality mask.
    
    Args:
        initial_prob_mask (torch.Tensor): Initial mask probabilities of shape [H, W] 
                                          or [1, 1, H, W], with values in [0, 1].
        image (torch.Tensor or Any): The input keyframe image format expected by sam_model.
        sam_model (nn.Module or Callable): The Segment Anything Model that takes 
                                           (image, box) as input.
        epsilon (float): Threshold for the gated confidence mask.
        
    Returns:
        torch.Tensor: The final high-quality reference mask generated by SAM.
    """
    # Ensure mask is [H, W] for calculations
    M = initial_prob_mask.squeeze()
    assert M.ndim == 2, "initial_prob_mask should be 2D after squeezing"
    
    # 1. Entropy Refinement
    # H_ij = - [ M_ij * log2(M_ij + 1e-7) + (1 - M_ij) * log2(1 - M_ij + 1e-7) ]
    # Clamp values to avoid log2(0) beyond 1e-7 and maintain numerical stability
    M_safe = torch.clamp(M, 1e-7, 1.0 - 1e-7)
    H = - (M_safe * torch.log2(M_safe) + (1.0 - M_safe) * torch.log2(1.0 - M_safe))
    
    # Gated Confidence Mask C_ij = 1 if M_ij * (1 - H_ij) > epsilon else 0
    confidence = M * (1.0 - H)
    C = (confidence > epsilon).to(torch.float32)
    
    # 2. Bounding Box Generation
    # Fallback if no confident pixels are found to avoid crash
    if C.sum() == 0:
        fallback_mask = (M > 0.5).to(torch.float32)
        if fallback_mask.sum() == 0:
            return M  # No valid objects found, return initial mask
        else:
            C = fallback_mask
            
    # ops.masks_to_boxes expects mask of shape [N, H, W]
    # It returns boxes in [xmin, ymin, xmax, ymax] format
    boxes = ops.masks_to_boxes(C.unsqueeze(0))  # Shape: [1, 4]
    box = boxes[0]
    
    # Extract coordinates
    xmin, ymin, xmax, ymax = box[0].item(), box[1].item(), box[2].item(), box[3].item()
    
    # Format per user requirement: [ymin, xmin, ymax, xmax]
    # (Note: Most SAM implementations expect [xmin, ymin, xmax, ymax], adapt as needed
    # when calling your specific SAM implementation's forward pass)
    sam_box = [ymin, xmin, ymax, xmax] 
    
    # 3. SAM Re-querying
    # Prompt the SAM model with the extracted bounding box and the key frame image.
    # Depending on the wrapper, it might expect a list or a tensor.
    final_mask = sam_model(image, box=sam_box)
    
    return final_mask


