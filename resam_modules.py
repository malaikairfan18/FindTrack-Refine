# MODULE 2: Soft Semantic Alignment (SSA) Loss (Reinforce)
# ==============================================================================

class SoftSemanticAlignment(nn.Module):
    def __init__(self, queue_size=128, embedding_dim=256):
        """
        Soft Semantic Alignment (SSA) module to regularize temporal feature 
        propagation during the tracking/training phase.
        
        Args:
            queue_size (int): The number of historical embeddings to keep in the FIFO queue.
            embedding_dim (int): Dimensionality of the feature embeddings.
        """
        super(SoftSemanticAlignment, self).__init__()
        self.queue_size = queue_size
        self.embedding_dim = embedding_dim
        
        # 1. L2-Normalized Embedding Queue
        # We register the queue as a buffer so it is saved in the state_dict but is not 
        # a trainable parameter. Initialize with random values and L2 normalize.
        queue_init = torch.randn(queue_size, embedding_dim)
        queue_init = F.normalize(queue_init, p=2, dim=1)
        self.register_buffer('queue', queue_init)
        
        # Pointer for the FIFO queue to keep track of where to insert next
        self.register_buffer('ptr', torch.zeros(1, dtype=torch.long))

    @torch.no_grad()
    def _dequeue_and_enqueue(self, keys):
        """
        Update the FIFO queue with new keys (historical embeddings).
        
        Args:
            keys (torch.Tensor): New L2-normalized embeddings to add. Shape: [batch_size, embedding_dim]
        """
        batch_size = keys.shape[0]
        ptr = int(self.ptr)
        
        # Handle queue updates depending on batch size
        if ptr + batch_size <= self.queue_size:
            self.queue[ptr:ptr + batch_size] = keys
            ptr = (ptr + batch_size) % self.queue_size
        else:
            # Wrap-around case
            remain = self.queue_size - ptr
            self.queue[ptr:] = keys[:remain]
            self.queue[:batch_size - remain] = keys[remain:]
            ptr = batch_size - remain

        self.ptr[0] = ptr

    def forward(self, current_embeddings, update_queue=True):
        """
        Calculates the SSA loss between current embeddings and the historical queue.
        
        Args:
            current_embeddings (torch.Tensor): Features from the current frame. 
                                               Shape: [batch_size, embedding_dim]
            update_queue (bool): If True, enqueues current embeddings into the historical queue.
            
        Returns:
            torch.Tensor: The computed Loss_SSA scalar.
        """
        # 1. L2-Normalize current embeddings (\hat{h} = h / ||h||_2)
        h_hat = F.normalize(current_embeddings, p=2, dim=1)  # [batch_size, embedding_dim]
        
        # Fetch historical embeddings (\hat{s}) which are already normalized
        # Detach to prevent gradients from flowing back into historical states
        s_hat = self.queue.clone().detach()  # [queue_size, embedding_dim]
        
        # 2. SSA Loss Function
        # Calculate cosine similarity: h_hat * s_hat.T -> [batch_size, queue_size]
        # Represents \hat{s}_i^T * \hat{h}_i for all combinations
        similarity = torch.matmul(h_hat, s_hat.T) 
        
        # Loss_SSA = (1 / q) * sum(1 - \hat{s}_i^T * \hat{h}_i)
        # We average over both the queue size and the batch size
        loss_ssa = (1.0 - similarity).mean()
        
        # 3. Update the historical queue with the new frame embeddings
        if update_queue:
            self._dequeue_and_enqueue(h_hat.detach())
            
        return loss_ssa
